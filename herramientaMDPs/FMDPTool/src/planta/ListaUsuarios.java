    public void setDatos(){

        manager.UserBean ub = new manager.UserBean(DatosPersistence.emf);
        List<entity.Usuario> ue = ub.findAll();
        
        if(ue.size() <= 0)
            JOptionPane.showMessageDialog(this, "No existen usuarios registrados en la base de datos");
        else{

           for(int i=0;i<ue.size();i++){
                   tablaHist.setValueAt(ue.get(i).getNombre()+ " "+ ue.get(i).getApaterno() + " "+ ue.get(i).getAmaterno(), i, 0);
                   tablaHist.setValueAt(ue.get(i).getNombreUsuario(), i, 1);
                   tablaHist.setValueAt(ue.get(i).getContra(), i, 2);
           }


        }
            

       


    }

   

